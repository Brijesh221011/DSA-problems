
public class Duplicate {
    public List<Integer> findDuplicates(int[] nums) {
        List<Integer>res=new ArrayList<>();
      
        for(int num:nums){
          int idx=Math.abs(num)-1;
          if(nums[idx]<0){
            res.add(idx+1);
          }else{
            nums[idx]=-nums[idx];
          }
        }
        return res;
    }
}
